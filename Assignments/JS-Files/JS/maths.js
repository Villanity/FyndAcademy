// JavaScript has a variety of built-in math methods that can be used for various calculations. Here are some of the most commonly used ones:

// Math.abs(x): Returns the absolute value of x, which is the positive value of x without its sign.

// Math.ceil(x): Returns the smallest integer greater than or equal to x.

// Math.floor(x): Returns the largest integer less than or equal to x.

// Math.round(x): Returns the value of x rounded to the nearest integer.

// Math.max(x, y, z, ...) and Math.min(x, y, z, ...): Returns the largest and smallest value among the given numbers respectively.

// Math.pow(x, y): Returns the value of x raised to the power of y.

// Math.sqrt(x): Returns the square root of x.

// Math.random(): Returns a random number between 0 (inclusive) and 1 (exclusive).

// Math.PI: Returns the value of pi, approximately 3.14159.

// Math.sin(x), Math.cos(x), and Math.tan(x): Returns the sine, cosine, and tangent of x in radians.

// Math.atan2(y, x): Returns the arctangent of the quotient of its arguments.

// Math.log(x), Math.log10(x), and Math.exp(x): Returns the natural logarithm, base-10 logarithm, and exponential function of x respectively.

// Math.floor(Math.random() * (max - min + 1) + min): Returns a random integer between min (inclusive) and max (inclusive).

// Here are some examples of how each of the math methods can be used in JavaScript:

// Math.abs(x):

Math.abs(-5); // returns 5
Math.abs(5); // returns 5

// Math.ceil(x):

Math.ceil(4.3); // returns 5
Math.ceil(4.9); // returns 5

// Math.floor(x):

Math.floor(4.3); // returns 4
Math.floor(4.9); // returns 4

// Math.round(x):

Math.round(4.3); // returns 4
Math.round(4.9); // returns 5

// Math.max(x, y, z, ...) and Math.min(x, y, z, ...):

Math.max(10, 5, 15); // returns 15
Math.min(10, 5, 15); // returns 5

// Math.pow(x, y):

Math.pow(2, 3); // returns 8

// Math.sqrt(x):

Math.sqrt(25); // returns 5

// Math.random():

Math.random(); // returns a random number between 0 and 1 (e.g. 0.123456789)

// Math.PI:

Math.PI; // returns 3.141592653589793

// Math.sin(x), Math.cos(x), and Math.tan(x):

Math.sin(Math.PI/2); // returns 1 (the sine of 90 degrees)
Math.cos(Math.PI); // returns -1 (the cosine of 180 degrees)
Math.tan(Math.PI/4); // returns 1 (the tangent of 45 degrees)

// Math.atan2(y, x):

Math.atan2(1, 1); // returns 0.7853981633974483 (the arctangent of 1/1)

// Math.log(x), Math.log10(x), and Math.exp(x):

Math.log(Math.E); // returns 1 (the natural logarithm of e)
Math.log10(1000); // returns 3 (the base-10 logarithm of 1000)
Math.exp(2); // returns 7.38905609893065 (the exponential function of 2)

// Math.floor(Math.random() * (max - min + 1) + min):

Math.floor(Math.random() * (10 - 1 + 1) + 1); // returns a random integer between 1 and 10 (inclusive)

